package com.bilgeadam.basicconseps;

public class ConditionalExample {

    public static void main(String[] args) {

        int a = 9;
        int b = 9;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
    }
}